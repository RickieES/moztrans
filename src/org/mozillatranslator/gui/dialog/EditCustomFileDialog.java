    private void realButtonPressed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_realButtonPressed
    {//GEN-HEADEREND:event_realButtonPressed
        File defaultFile;
        JFileChooser chooser;
        int result;
        
        defaultFile = new File(realField.getText());
        File f = defaultFile.isFile() ? defaultFile :
            new File(Kernel.settings.getString(Settings.GUI_IMPORT_FILE_CHOOSER_PATH));
            chooser = new JFileChooser(f);
            
            chooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
            chooser.setDialogTitle("Select original file ");
            chooser.setSelectedFile(defaultFile);
            
            result = chooser.showDialog(this,"Choose");
            if (result == JFileChooser.APPROVE_OPTION)
            {
                Kernel.settings.setString(Settings.GUI_IMPORT_FILE_CHOOSER_PATH,
                chooser.getCurrentDirectory().getAbsolutePath());
                defaultFile = chooser.getSelectedFile();
                realField.setText(defaultFile.toString());
            }
            
    }//GEN-LAST:event_realButtonPressed
    
