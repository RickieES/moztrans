    public boolean showDialog()
    {
        okay = false;
        nameField.setText(custom.getName());
        realField.setText(custom.getRealFilename());
        relativeField.setText(custom.getRelativeFilename());
        setVisible(true);
        
        if (okay)
        {
            custom.setName(nameField.getText());
            custom.setRealFilename(realField.getText());
            custom.setRelativeFilename(relativeField.getText());
        }
        dispose();
        return okay;
        
    }
    
    
